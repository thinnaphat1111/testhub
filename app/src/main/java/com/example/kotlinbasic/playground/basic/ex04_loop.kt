package com.example.kotlinbasic.playground.basic
//เรื่องสุดท้ายละไอ้สาสสสสสสสสสสสสสสสสสสสสสส
    fun main(){
        for (count in 1..10){
            print("$count")
        } //มันจะปริ้น  1-10
        println()
        for (count in 0 until 10){
            print("$count")
        }  // เหมือนกันเลยไอ้สาสแค่เขียนคนละแบบ -*-
        println()
        for (count in 10 downTo 0){
            print("$count")
        } //นับย้อน
        println()
        for (count in 0..10 step 2){
            print("$count")
        } //นับเพิ่มทีละ 2
        println()
    //จบบบบบบบบบบบบบบบบบบบบบบบบ
}
